package Casting;

public class ClassCast {
    public static void main(String[] args) {
        String string = "10";
        int num = Integer.parseInt(string);
        System.out.println(num);

        String string1 = "java";
        int num2 = Integer.parseInt(string1);
        System.out.println("num2 = " + num2);
    }
}
