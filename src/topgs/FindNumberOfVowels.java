package topgs;

public class FindNumberOfVowels {
    public static void main(String[] args) {
        String arr[] = {"a", "e", "y", "u", "h", "d"};
        for (int i = 0; i < args.length; i++) {
            if (arr[i] == "a,e,i,o,u") {
                System.out.println();
            }
        }
    }
}
